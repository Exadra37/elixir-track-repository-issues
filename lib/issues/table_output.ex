defmodule Issues.TableOutput do

  def header() do
    IO.write """

    --------+----------------------+-------------------------------------------------------------------
        #   | created_at           | title
    --------+----------------------+-------------------------------------------------------------------
    """
  end

  def row(number, created_at, title) do
    IO.puts "   #{number} | #{created_at} | #{title}"
  end

  def footer() do
    IO.write """
    --------+----------------------+-------------------------------------------------------------------

    """
  end

end
